const numbers = [1, 2, 3];

//join array element by ;
const joined = numbers.join(";");
console.log(joined);

// using split to split only where have the space
const message = "This is my first message";
const parts = message.split(" ");
console.log(parts);

// using join to make it join with -
const combined = parts.join("-");
console.log(combined);
